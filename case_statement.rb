my_weather = "sunny"
case my_weather
	when "sunny" then puts "Oh Happy Day!"
	when "cloudy" then puts "A break from the sun!"
	when "cold" then puts "Get a jacket!"
	when "rainy" then puts "Don't forget the Umbrella!"
else
	puts "I guess we'll have to go outside to find out."
end
